public class MovieManager {
    
    private int menuSChoice;
    
    public void setMenuSChoice(int choice)
    {
        menuSChoice = choice;
    }
    
    public void staffMenu()
    {
        switch(menuSChoice)
        {
            case 1: //Update Movie Listing
                break;
            case 2: //Create Movie
                break;
            case 3: //Update Movie
                break;
            case 4: //Delete Movie
                break;
            case 5: //Configure System Settings
                break;
            case 6: //Back
                break;
            case 7: //Logout
                break;
            case 8: //Exit
                break;
        }
    }
    
    
}
